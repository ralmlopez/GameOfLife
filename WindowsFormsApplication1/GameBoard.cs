        private int[,] GetSeedArray(string fileName)
        {
            var filePath = string.Format(@"c:\GameOfLife\{0}.txt", fileName);

            var input = File.ReadAllLines(filePath);

            var rowCount = input.Length;
            var columnCount = input[0].Split(',').Select(sValue => sValue.Trim()).Count();

            var rowIndex = 0;
            var result = new int[rowCount, columnCount];

            foreach (var row in input)
            {
                var columnIndex = 0;
                foreach (var col in row.Split(',').Select(value => value.Trim()))
                {
                    result[rowIndex, columnIndex] = int.Parse(col);
                    columnIndex++;
                }
                rowIndex++;
            }

            return result;
        }

        private void Start_Click(object sender, EventArgs e)
        {
            var seed = GetSeedArray(Seed.SelectedItem.ToString());

            var scale = int.Parse(Scale.Text);
            var delay = int.Parse(Delay.Text);

            _game = new Game(seed, scale, delay);
            _game.NewGenerationCreated += game_NewGenerationCreated;

            Start.Enabled = false;
            var starter = new ThreadStart(_game.Simulate);
            var thread = new Thread(starter);
            thread.Start();
        }

        private void game_NewGenerationCreated(object sender, NewGenerationEventArgs e)
        {
            if (InvokeRequired)
            {
                Invoke(new UpdateUiCallback(UpdateUi), new object[] { e.Bitmap });
            }
            else
            {
                UpdateUi(e.Bitmap);
            }
        }

        private void UpdateUi(Bitmap bitmap)
        {
            GameImage.Image = bitmap;
            GameImage.Invalidate();
            GameImage.Update();

        }

        private void Stop_Click(object sender, EventArgs e)
        {
            Start.Enabled = true;
            _game.NewGenerationCreated -= game_NewGenerationCreated;
            _game.Stop();
        }






